let numeros=prompt("dime un numero romano");

if(numeros=="I"){
    alert(numeros+" es igual a 1");
}else if(numeros=="V"){
    alert(numeros+" es igual a 5");
}else if(numeros=="X"){
    alert(numeros+" es igual a 10");
}else if(numeros=="L"){
    alert(numeros+" es igual a 50");
}else if(numeros=="C"){
    alert(numeros+" es igual a 100");
}else if(numeros=="M"){
    alert(numeros+" es igual a 1000");
}else{
    alert("error");
}